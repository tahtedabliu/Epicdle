// ================= DATA LOCAL =================
function getDataLocal(){
const hoje = new Date()
const ano = hoje.getFullYear()
const mes = String(hoje.getMonth()+1).padStart(2,"0")
const dia = String(hoje.getDate()).padStart(2,"0")
return `${ano}-${mes}-${dia}`
}
